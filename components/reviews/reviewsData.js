// Extract unique service types for filtering
export const getServiceTypes = () => {
  return [...new Set(reviewsData.map(review => review.service))];
};

// Calculate review statistics
export const getReviewStats = (reviews = reviewsData) => {
  const totalReviews = reviews.length;
  const averageRating = reviews.reduce((sum, review) => sum + review.rating, 0) / totalReviews;
  const satisfactionRate = reviews.filter(review => review.rating === 5).length / totalReviews * 100;
  
  return {
    totalReviews,
    averageRating: parseFloat(averageRating.toFixed(1)),
    satisfactionRate: Math.round(satisfactionRate)
  };
};
